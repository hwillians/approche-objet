package maps;

public class Salarie {
	
	String nom;
	int salaire;
	public Salarie(String nom, int salaire) {
		super();
		this.nom = nom;
		this.salaire = salaire;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public int getSalaire() {
		return salaire;
	}
	public void setSalaire(int salaire) {
		this.salaire = salaire;
	}
	
	

	

}
